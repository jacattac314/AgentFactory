class _VisionAdapter:
    """Analyses screenshots using the Anthropic vision API."""

    def __init__(self) -> None:
        import anthropic  # lazy: not needed in dry-run
        self._client = anthropic.Anthropic()  # reads ANTHROPIC_API_KEY from env

    def analyze(self, screenshot_bytes: bytes, prompt: str) -> dict:
        b64 = base64.b64encode(screenshot_bytes).decode()
        msg = self._client.messages.create(
            model="claude-opus-4-7",
            max_tokens=256,
            messages=[{
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "source": {
                            "type": "base64",
                            "media_type": "image/png",
                            "data": b64,
                        },
                    },
                    {
                        "type": "text",
                        "text": prompt + "\n\nRespond with valid JSON only, no other text.",
                    },
                ],
            }],
        )
        raw = msg.content[0].text.strip()
        try:
            return json.loads(raw)
        except json.JSONDecodeError:
            match = re.search(r'\{.*\}', raw, re.DOTALL)
            if match:
                try:
                    return json.loads(match.group())
                except json.JSONDecodeError:
                    pass
        return {"found": False}


